##display natural numbers up to 10

print("Use of range () 1")
for i in range(10):         # Syntax: range(start, stop, step)
    print(i)                # displays 0 to 9

##display natural numbers from 4 to 8 (8 is not included in default)
print("Use of range () 2")
for i in range(4, 8):       #displays 4 to 7( including 7)
    print(i)

##display natural numbers from 4 to 10 and each next number should be two step ahead
print("Use of range () 3")
for i in range(4, 10, 2):
    print(i)                #displays 4 to 10, next number is 2 step ahead




























